package vo;

public class GeulVO {
	private int geul_num;
	private String geul_id;
	private String geul_content;
	private String start_date;
	private String modify_date;
	private String geul_post;
	public String getGeul_post() {
		return geul_post;
	}
	public void setGeul_post(String geul_post) {
		this.geul_post = geul_post;
	}
	public int getGeul_num() {
		return geul_num;
	}
	public void setGeul_num(int geul_num) {
		this.geul_num = geul_num;
	}
	public String getGeul_id() {
		return geul_id;
	}
	public void setGeul_id(String geul_id) {
		this.geul_id = geul_id;
	}
	public String getGeul_content() {
		return geul_content;
	}
	public void setGeul_content(String geul_content) {
		this.geul_content = geul_content;
	}
	public String getStart_date() {
		return start_date;
	}
	public void setStart_date(String start_date) {
		this.start_date = start_date;
	}
	public String getModify_date() {
		return modify_date;
	}
	public void setModify_date(String modify_date) {
		this.modify_date = modify_date;
	}
}
